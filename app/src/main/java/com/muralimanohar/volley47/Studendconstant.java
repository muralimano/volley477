package com.muralimanohar.volley47;

public class Studendconstant {

    public Studendconstant(String firstname, String secondname, String age) {
        this.firstname = firstname;
        this.secondname = secondname;
        this.age = age;
    }

    String firstname;
    String secondname;
    String age;

    public String getFirstname() {
        return firstname;
    }

    public String getSecondname() {
        return secondname;
    }

    public String getAge() {
        return age;
    }





}
